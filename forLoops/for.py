# Teaching for loops

# Iterating over a list with a for loops

list = [1, 2, "John", 5, "Abraham"]

for value in list:
    print(value)

# We create a list at the top and then use the for loop to get each element of said list
# The value variable can be set as anything. It can only be used inside the for loop. 
# Then, we call list as the variable we want to iterate through. 
# For each value in the list (for value in list:), we print what that value is.
